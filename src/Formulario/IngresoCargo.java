    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (descripcionCargo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Descripcion, es un campo obligatorio","Error",JOptionPane.ERROR_MESSAGE);
        } else if (sueldoCargo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Sueldo, es un campo obligatorio","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
                conectar con=new conectar();
                Connection reg= con.conexion();
                String des,sueldo;
                String sql;

                des=descripcionCargo.getText();
                sueldo=sueldoCargo.getText();

                sql="INSERT INTO cargo (Descripcion, Sueldo) VALUES (?,?)";
                try {
                    PreparedStatement pst= reg.prepareStatement(sql);
                    pst.setString(1, des);
                    pst.setString(2, sueldo);
                    int n =pst.executeUpdate();
                    if (n>0) {
                        JOptionPane.showMessageDialog(null, "Cargo Registrado Correctamente");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(IngresoCliente.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null,"Error "+ex);
                }
                cargar();
                bloquear();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

