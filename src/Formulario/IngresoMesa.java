    public void cargar(){
        String [] titulos={"CÃ³digo","Asientos","Estado"};
        String [] registros= new String[3];
        String sql= "SELECT idMesa,Asientos,Estado FROM mesa ";
        
        modelo = new DefaultTableModel(null, titulos);
        conectar cc= new conectar();
        Connection cn= cc.conexion();
        
        try {
            java.sql.Statement st= cn.createStatement();
            ResultSet rs= st.executeQuery(sql);
            while (rs.next()) {
                registros[0]= rs.getString("idMesa");
                registros[1]= rs.getString("Asientos");
                registros[2]= rs.getString("Estado");
                modelo.addRow(registros);
                tablaMesas.setModel(modelo);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(IngresoCargo.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    
    }
    public void eliminar(){
            conectar cc= new conectar();
            Connection cn= cc.conexion();
            try {
                int fila= tablaMesas.getSelectedRow();
                String sql = "DELETE from mesa WHERE idMesa="+tablaMesas.getValueAt(fila, 0);
                sent= cn.createStatement();
                int n= sent.executeUpdate(sql);
                if (n>0) {
                    cargar();
                    JOptionPane.showMessageDialog(null, "Datos Eliminados!!!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Error" + e.getMessage());
            }
    }
